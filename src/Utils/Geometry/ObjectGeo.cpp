#include <ObjectGeo.h>

/*************************************************************************************/
/*************************************************************************************/
namespace Utils 
{
/*************************************************************************************/
/*************************************************************************************/
ObjectGeo::ObjectGeo()
{
}
/*************************************************************************************/
/*************************************************************************************/
ObjectGeo::ObjectGeo(Vector3 Min, Vector3 Max)
{
	this->Min = Min;
	this->Max = Max;
}
/*************************************************************************************/
ObjectGeo::ObjectGeo(const ObjectGeo& O)
{
	this->Min = O.Min;
	this->Max = O.Max;
}
/*************************************************************************************/
ObjectGeo::~ObjectGeo()
{
}
/*************************************************************************************/
/*************************************************************************************/
Vector3 ObjectGeo::getMin()
{
	return Min;
}
/*************************************************************************************/
Vector3 ObjectGeo::getMax()
{
	return Max;
}
/*************************************************************************************/
/*************************************************************************************/
void ObjectGeo::setMin(Vector3 Min)
{
	this->Min = Min;
}
/*************************************************************************************/
void ObjectGeo::setMax(Vector3 Max)
{
	this->Max = Max;
}
/*************************************************************************************/
/*************************************************************************************/
}
